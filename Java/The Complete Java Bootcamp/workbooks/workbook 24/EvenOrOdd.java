
public class EvenOrOdd {
    public static void main(String[] args) {
        // Make a for loop that counts from 0 to 19.
        System.out.println();
        for (int i = 0; i < 20; i++) {
            if (i % 2 == 0) {
                System.out.println(i + " - even");
            } else {
                System.out.println(i + " - odd");
            }
        }
        System.out.println();
    }
 }
