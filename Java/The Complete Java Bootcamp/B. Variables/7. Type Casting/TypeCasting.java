
class TypeCasting {
    public static void main(String[] args) {
        // We can cast values to another type in Java
        double monthlySalary = 5834.98;
        int roundedSalary = (int)monthlySalary;

        System.out.println("Salary: " + monthlySalary);
        System.out.println("Rounded: " + roundedSalary);
    }
}
