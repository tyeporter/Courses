
class Strings {
    public static void main(String[] args) {
        // A string stores a string of characters
        String sentence = "This is a string";
        System.out.println(sentence);

        // String concatenation
        String myString = "Her name is: ";
        String placeholder = "Raven";
        System.out.println(myString + placeholder);

        int points = 50;
        String announcement = " points for Gryffindor!";
        System.out.println(points + announcement);
    }
}
